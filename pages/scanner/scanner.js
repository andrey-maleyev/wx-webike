  /**
   * Lifecycle function--Called when page load
   */
  onLoad: function (options) {
    const page = this
    const memory = getApp().globalData

    wx.scanCode({
      onlyFromCamera: true,
      success(res) {

        memory.bikeId = res.result
        console.log(memory.bikeId)

        page.setData({
          qrResult: memory.bikeId
        })
        wx.navigateTo({
          url: '/pages/trip/trip',
        })
      },
      fail(err) {
        console.log(err)
      }
    })
  },

